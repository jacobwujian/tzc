      let ul1 = document.getElementById('comment1')
      let ul2 = document.getElementById('comment2')
      let ulbox = document.getElementById('review_box')
       ul2.innerHTML = ul1.innerHTML
       ulbox.scrollTop = 0 // 开始无滚动时设为0
      const that = this
      that.timer = setInterval(that.rollStart, t) // 设置定时器，参数t用在这为间隔时间（单位毫秒），参数t越小，滚动速度越快
       // 鼠标移入div时暂停滚动
       ulbox.onmouseover = function() {
         clearInterval(that.timer)
        }
      // 鼠标移出div后继续滚动
       ulbox.onmouseout = function() {
         that.timer = setInterval(that.rollStart, t)
